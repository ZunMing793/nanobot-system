def has_chinese(text):
    return bool(re.search(r'[\u4e00-\u9fff]', text))
def fix_content(text):
    """Fix mojibake content."""
    fixed = text
    for garbled, correct in MOJIBAKE_PATTERNS.items():
        if garbled in fixed:
            fixed = fixed.replace(garbled, correct)
    return fixed
def main():
    import argparse
    parser = argparse.ArgumentParser(description='Fix mojibake files')
    parser.add_argument('--dry-run', action='store_true', help='Dry run only')
    parser.add_argument('path', type=str, help='Path to fix')
    args = parser.parse_args()
    base = Path(args.path)
    if not base.exists():
        print(f'Path not found: {base}')
        return
    files = []
    if base.is_file():
        files.append(base)
    else:
        files.extend(base.rglob('*.md'))
    print(f'Processing {len(files)} files...')
    stats = {'total': len(files), 'fixed': 0}
    fixed_files = []
    for f in sorted(files):
        try:
            content = f.read_text(encoding='utf-8')
            fixed = fix_content(content)
            if fixed != content:
                stats['fixed'] += 1
                fixed_files.append(f.name)
                if not args.dry_run:
                    f.write_text(fixed, encoding='utf-8', newline='\n')
        except Exception as e:
            print(f'[ERR] {f.name}: {e}')
    print(f'\nTotal: {stats["total"]} files')
    print(f'Fixed: {stats["fixed"]} files')
    if fixed_files:
        print('\nFixed files:')
        for name in fixed_files[:20]:
            print(f'  {name}')
        if len(fixed_files) > 20:
            print(f'  ... and {len(fixed_files) - 20} more')
if __name__ == '__main__':
    main()
